// Aula Objeto Math

let num1 = 9.54578;
let num2 = Math.floor(num1); // Arredonda para baixo.
console.log(num2);

let num3 = 9.54578;
let num4 = Math.ceil(num3); // Arredonda para cima.
console.log(num4);

let num5 = 9.54578;
let num6 = Math.round(num5); // Arredonda para o número mais próximo acima de 50 ou abaixo.
console.log(num6);